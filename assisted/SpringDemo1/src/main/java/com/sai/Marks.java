package com.sai;

public class Marks {
	float mat;
	float sci;
	float bio;
	public float getMat() {
		return mat;
	}
	public void setMat(float mat) {
		this.mat = mat;
	}
	public float getSci() {
		return sci;
	}
	public void setSci(float sci) {
		this.sci = sci;
	}
	public float getBio() {
		return bio;
	}
	public void setBio(float bio) {
		this.bio = bio;
	}
	

}
